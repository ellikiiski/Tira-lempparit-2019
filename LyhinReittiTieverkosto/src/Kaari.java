
public class Kaari {
	private int mista;
	private int minne;
	private long matka;

	public Kaari(int mista, int minne, long matka) {
    	this.mista = mista;
    	this.minne = minne;
    	this.matka = matka;
	}

   	public int getMista() {
    	return mista;
	}

	public void setMista(int mista) {
    	this.mista = mista;
	}

	public int getMinne() {
    	return minne;
	}

	public void setMinne(int minne) {
    	this.minne = minne;
	}

	public long getMatka() {
    	return matka;
	}

	public void setMatka(long matka) {
    	this.matka = matka;
	}
}
